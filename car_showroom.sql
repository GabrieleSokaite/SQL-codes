--What are the details of all cars purchased in the year 2022?

Select b.*
FROM sales a
JOIN cars b ON a.car_id = b.car_id
WHERE a.purchase_date >= '2022-01-01' AND a.purchase_date < '2023-01-01';


--What is the total number of cars sold by each salesperson?
select b.name as salesman, COUNT(a.car_id) as sales
FROM sales a 
JOIN salespersons b On a.salesman_id = b.salesman_id
group by b.salesman_id;


--What is the total revenue generated by each salesperson?
select c.name, sum(b.cost_$) as revenue
FROM sales a 
JOIN cars b On a.car_id = b.car_id
join salespersons c on c.salesman_id = a.salesman_id
group by c.name;


--What are the details of the cars sold by each salesperson?

SELECT a.name as salesperson, c.*
from salespersons a 
