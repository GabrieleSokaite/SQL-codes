--What is the name and city of the salesperson who sold the most number of cars in the year 2023?

SELECT a.name, a.city, COUNT(car_id) as sold_cars, b.purchase_date
from salespersons a 
join sales b on a.salesman_id = b.salesman_id 
WHERE b.purchase_date >= '2023-01-01' AND b.purchase_date < '2024-01-01'
GROUP by a.name and a.city
ORDER BY sold_cars DESC
LIMIT 1;


--What is the name and age of the salesperson who generated the highest revenue in the year 2022?

SELECT a.name, a.age, SUM(c.cost_$) as revenue, b.purchase_date
from salespersons a 
